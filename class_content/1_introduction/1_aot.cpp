// q.WAP to calculate area of triangle
#include<iostream>
using namespace std;
int main (){
    int base,height,area;
    cout <<"Enter value of base-";
    cin>>base;
    cout <<"Enter value of height-";
    cin>>height;
    area =0.5*(base * height);
    cout <<"Area of triangle is"<<area;

    return 0;


}